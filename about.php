                中国长久以来都被水污染问题所困扰，其污染主要来源包括无休止的工业排放以及肥料和杀虫剂在农业上的过度使用。尽管国内媒体在水污染问题报道的数量和态度上已然逐渐开放，但普通公众依旧只能掌握相当有限的信息。受到社交媒体对空气质量（主要为PM2.5）的报道激起公众反响并最终致使空气质量数据的透明度增加这一事件的启发，几名来自于CYCAN国际部的致力于改善中国环境的青年人发起了“MyH<sub>2</sub>O”——中国第一批线上水质信息众包平台之一。来自于麻省理工大学，哥伦比亚大学，和几所著名文理学院的他们拥有各种与环境相关的专业知识，如环境化学，环境工程和政策，计算机软件开发，和数据可视化处理与模型搭建等等。运用自己的知识，接受专业教授的指导，他们已经发展了比较完善的项目计划，并且得到了麻省理工大学的资金支持。
            </p>
            <br>
            <p class="text-justify" style="font-size:18px">
                Affordable water quality testing kits and water investigation handbooks will be distributed to university students and community volunteers, who will be trained by MyH<sub>2</sub>O as part of a network to test water quality, evaluate water safety and offer clean water advice in areas of public concern. During the first phase of the project, MyH<sub>2</sub>O will focus on suburban and rural non-tapping drinking water sources around Beijing that haven’t been monitored regularly. Immediate fieldwork investigation results, locations and photos of water bodies will feed into a common mapping database through computer-based and mobile app platforms and visualized in an innovative format with a tangible meaning to the public. Connected to youth across China through CYCAN’s network, our water quality testing network and online mapping platform have the great potential to engage thousands of people and collectively raise public awareness about water quality.
            </p>
            <br>
            <p class="text-justify" style="font-size:18px">
                MyH<sub>2</sub>O是国内少有的公众信息搭建的中国水质数据网络平台之一，帮助中国居民了解到自己身边饮用水与地表水的健康情况，旨在通过独立水质报告激发对不良水质危害的认识，增加信息公开透明度并引发公众对解决方案的思考。通过水质检测工具包的发放、生物水质检测法的教育、以及来自科研与政府的数据收集，项目搭建了一个公众的水质地图平台，方便任何人来查找身边饮用水以及地表水的水质信息以及相应的安全等级。我们希望帮助在中国的民众更好地认识到自己周围水的质量，以及对于健康的影响，并鼓励群众采取相应的措施来保护自己的饮用水等水资源（如增加过滤装置），增加水管理，提升全国总体水质量。
