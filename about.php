                China has long been troubled by water contamination, due in large part to continuous emissions from industries and heavy use of agricultural fertilizers and pesticides. Although the media has become increasingly open in China's water problem, the public is still only presented with limited information on water quality. Inspired by the air quality (PM 2.5) campaigns on social media that stirred up public reaction and eventually greatly increased the transparency of air quality information, we are developing "MyH<sub>2</sub>O" as one of the first online crowdsourcing platforms on China's water quality. Created in partnership with China Youth Climate Action Network (CYCAN), MyH<sub>2</sub>O aims to prompt water risk awareness, increase information transparency and motivate citizen solutions through independent reports of water quality.
